bucketList = ['Salem', 'Ireland', 'Japan', 'Chicago', 'Outer Space']

#Original List
print(bucketList)

#Temporary sort using sorted()
print("\n")
print(sorted(bucketList))

#reverse()
bucketList.reverse()
print("\n")
print(bucketList)

bucketList.reverse()
print("\n")
print(bucketList)

#sort()
bucketList.sort()
print("\n")
print(bucketList)

#Reverse sort()
bucketList.sort(reverse=True)
print("\n")
print(bucketList)